
public class Coordinates {
	private char x;
	private int y;
	
	public Coordinates(char row, int col) {
		x = row;
		y = col;
	}

	public char getX() {
		return x;
	}

	public void setX(char x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}
	
	
}
